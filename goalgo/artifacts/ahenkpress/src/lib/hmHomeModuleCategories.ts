/** Manuel slug sırasına göre kategori bölümlerini seçer (renkli şerit vb.). */
export function pickCategorySectionsBySlugs<T extends { slug: string }>(
  sections: readonly T[] | null | undefined,
  orderedSlugs: readonly string[] | null | undefined,
): T[] {
  const safeSections = Array.isArray(sections) ? sections : [];
  const safeOrderedSlugs = Array.isArray(orderedSlugs) ? orderedSlugs : [];
  if (safeOrderedSlugs.length === 0) return [...safeSections];
  const bySlug = new Map<string, T>();
  for (const section of safeSections) {
    const slug = hmCategorySlug(section.slug);
    if (slug && !bySlug.has(slug)) bySlug.set(slug, section);
  }
  const out: T[] = [];
  for (const raw of safeOrderedSlugs) {
    const slug = hmCategorySlug(raw);
    const row = slug ? bySlug.get(slug) : undefined;
    if (row) out.push(row);
  }
  return out;
}

